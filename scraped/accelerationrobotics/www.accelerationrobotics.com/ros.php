                <div class="col-12 text-center py-5">
                    <h2>Why is ROS so important?</h2>
                    <img src="img/ros-grafo.jpg" alt="" class="img-responsive my-5">
                    <p>
                        Robots are networks of networks, with sensors capturing data, passing to compute technologies,
                        and then on to actuators and back again in a deterministic manner. These networks can be
                        understood as the nervous system of the robot, passing across compute <i>Nodes</i>, that
                        represent <i>neurons</i>. Like the human nervous systems, real-time information across all these
                        computational <i>Nodes</i> is fundamental for the robot to behave coherently.
                    </p>
                    <p>
                        "Robot brains" are built with this same philosophy. Behaviors take the form of computational
                        graphs, with data flowing between Nodes, across physical networks (communication buses) and
                        while mapping to underlying sensors, compute technologies and actuators. <strong>ROS enables you
                            to build these computational graphs and create robot behaviors</strong> by providing
                        libraries, a communication infrastructure, drivers and tools to put it all together.
                    </p>
                </div>
