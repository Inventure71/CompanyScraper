                    <p class="mt-3 pb-4 f-blanco">
                        ROS is the common language roboticists use to build robots. It's an open source and modular set
                        of software libraries and tools that help you build robot applications. It provides a
                        message-passing system between software components, drivers for robot hardware, state-of-the-art
                        robotic algorithms, and powerful developer tools to test, debug and visualize your robot.
                    </p>
